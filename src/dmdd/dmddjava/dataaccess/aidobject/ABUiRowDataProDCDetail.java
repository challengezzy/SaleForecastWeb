package dmdd.dmddjava.dataaccess.aidobject;

import java.io.Serializable;

import dmdd.dmddjava.common.constant.SysConst;

public class ABUiRowDataProDCDetail implements Serializable {
	private static final long serialVersionUID = 1L;
	private int period = SysConst.PERIOD_NULL;
	private Long sellIn = SysConst.NULL_NUMERICAL_FIELD_Long;
	private Long sellOut = SysConst.NULL_NUMERICAL_FIELD_Long;
	private Long termEnd = SysConst.NULL_NUMERICAL_FIELD_Long;
	private Long termBegin = SysConst.NULL_NUMERICAL_FIELD_Long;
	private Long stockDay = SysConst.NULL_NUMERICAL_FIELD_Long;

	public int getPeriod() {
		return period;
	}

	public void setPeriod(int period) {
		this.period = period;
	}

	public Long getSellIn() {
		return sellIn;
	}

	public void setSellIn(Long sellIn) {
		this.sellIn = sellIn;
	}

	public Long getSellOut() {
		return sellOut;
	}

	public void setSellOut(Long sellOut) {
		this.sellOut = sellOut;
	}

	public Long getTermEnd() {
		return termEnd;
	}

	public void setTermEnd(Long termEnd) {
		this.termEnd = termEnd;
	}

	public Long getTermBegin() {
		return termBegin;
	}

	public void setTermBegin(Long termBegin) {
		this.termBegin = termBegin;
	}

	public Long getStockDay() {
		return stockDay;
	}

	public void setStockDay(Long stockDay) {
		this.stockDay = stockDay;
	}

}
