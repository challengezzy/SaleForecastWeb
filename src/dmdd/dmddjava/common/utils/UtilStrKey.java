	public static String getStrKey4PPcOOcPB( BProduct _bProduct, BProductCharacter _bProductCharacter, 
			BOrganization _bOrganization, BOrganizationCharacter _bOrganizationCharacter,
			int _period,
			BBizData _bBizData )
	{
		String strKey = "";

		if ( _bProduct != null )
		{
			strKey = strKey + "_" + _bProduct.getId();
		}
		else
		{
			strKey = strKey + "_" + "-1";
		}

		if ( _bProductCharacter != null )
		{
			strKey = strKey + "_" + _bProductCharacter.getId();
		}
		else
		{
			strKey = strKey + "_" + "-1";
		}

		if ( _bOrganization != null )
		{
			strKey = strKey + "_" + _bOrganization.getId();
		}
		else
		{
			strKey = strKey + "_" + "-1";
		}

		if ( _bOrganizationCharacter != null )
		{
			strKey = strKey + "_" + _bOrganizationCharacter.getId();
		}
		else
		{
			strKey = strKey + "_" + "-1";
		}

		if( _period != SysConst.PERIOD_NULL )
		{
			strKey = strKey + "_" + _period;
		}
		else
		{
			strKey = strKey + "_" + "-1";
		}
		
		if ( _bBizData != null )
		{
			strKey = strKey + "_" + _bBizData.getId();
		}
		else
		{
			strKey = strKey + "_" + "-1";
		}

		return strKey;
	}
	
